public class JavaHashCodeExamples {
    public static void main(String[] args) {
        String a = "荣誉";
        String b = "reputation";
        String c = "20231107";
        String d = "双精度";
        System.out.println(a.hashCode());
        System.out.println(b.hashCode());
        System.out.println(c.hashCode());
        System.out.println(d.hashCode());
    }
}
